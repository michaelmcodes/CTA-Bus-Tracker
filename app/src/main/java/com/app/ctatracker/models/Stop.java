package com.app.ctatracker.models;

public class Stop {
    private String stpid;
    private String stpnm;
    private double lat;
    private double lon;


    public String getStpid() {
        return stpid;
    }

    public String getStpnm() {
        return stpnm;
    }

    public double getLat() {
        return lat;
    }

    public double getLon() {
        return lon;
    }
}
